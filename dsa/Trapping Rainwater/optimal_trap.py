arr=[0,1,0,2,1,0,1,3,2,1,2,1]
n=len(arr)
leftmax,rightmax,total=0,0,0
l=0
r=n-1
while(l<r):
    if(arr[l]<=arr[r]):
        if(leftmax>arr[l]):
            total+=leftmax-arr[l]
        else:
            leftmax=arr[l]
        l+=1
    else:
        if(rightmax>arr[r]):
            total+=rightmax-arr[r]
        else:
            rightmax=arr[r]
        r-=1
print(total," units")
